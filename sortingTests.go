package ipsorting

import (
	"log"
	"math/rand"
	"strconv"
)

func generateIP() string{
	var ip string
	ip = strconv.Itoa(rand.Intn(999))
	for i:=0; i<2; i++ {
		ip += "."
		ip += strconv.Itoa(rand.Intn(999))
	}
	ip += ":"
	ip += strconv.Itoa(rand.Intn(9999))
	return ip
}

func main() {
	var ipSlice[] string
	for i:=0; i<25; i++ {
		ipSlice = append(ipSlice, generateIP())
	}
	log.Println("initial unsorted list:")
	log.Println(ipSlice)
	log.Println("sorted list:")
	ipSlice = SortIPs(ipSlice)
	log.Println(ipSlice)
	log.Println("list after inserting '10.0.0.23:8070' :")
	ipSlice = InsortIP(ipSlice, "10.0.0.23:8070")
	log.Println(ipSlice)
	log.Println("list after inserting '101.0.2.23:22' :")
	ipSlice = InsortIP(ipSlice, "101.0.2.23:22")
	log.Println(ipSlice)
	log.Println("list after inserting '999.999.999.999:9999' :")
	ipSlice = InsortIP(ipSlice, "999.999.999.999:9999")
	log.Println(ipSlice)
	log.Println("list after inserting '0.0.0.0:0' :")
	ipSlice = InsortIP(ipSlice, "0.0.0.0:0")
	log.Println(ipSlice)

	log.Println("Tests complete.")
}

